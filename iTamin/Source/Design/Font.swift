import UIKit

extension UIFont {
    static func notoBlack(size: CGFloat = 14) -> UIFont {
        return UIFont(name: "NotoSansKR-Black", size: size)!
    }
    static func notoBold(size: CGFloat = 14) -> UIFont {
        return UIFont(name: "NotoSansKR-Bold", size: size)!
    }
    static func notoLight(size: CGFloat = 14) -> UIFont {
        return UIFont(name: "NotoSansKR-Light", size: size)!
    }
    static func notoMedium(size: CGFloat = 14) -> UIFont {
        return UIFont(name: "NotoSansKR-Medium", size: size)!
    }
    static func notoRegular(size: CGFloat = 14) -> UIFont {
        return UIFont(name: "NotoSansKR-Regular", size: size)!
    }
    static func notoThin(size: CGFloat = 14) -> UIFont {
        return UIFont(name: "NotoSansKR-Thin", size: size)!
    }
    static func SDGothicBold(size: CGFloat = 14) -> UIFont {
        return UIFont(name: "AppleSDGothicNeo-Bold", size: size)!
    }
    static func SDGothicMedium(size: CGFloat = 14) -> UIFont {
        return UIFont(name: "AppleSDGothicNeo-Medium", size: size)!
    }
}
